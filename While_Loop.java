public class While_Loop {
  public static void main(String[] args) {
    int i = 1;
    while( i <= 5){
      System.out.println(i+" "+"NIKHILESH");
      i++;
    }

    int sum = 0;
    int j = 1;
    while(j<=10){
      sum+=j;
      j++;
    }
    System.out.println("Sum of 1 to 10 is :" + sum);
  }
}
